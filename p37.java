// WAP to print  Upper case to Lower case ;
import java.util.*;

class p37{
	public static void main(String [] args) {
		String name;
		Scanner sc=new  Scanner(System.in);
		System.out.print("Enter your name : ");
		name=sc.nextLine();
		
		System.out.println("Your name is upper case: " +name.toUpperCase());
		System.out.println("Your name is upper case: " +name.toLowerCase());
		System.out.println("Your name is upper case: " +name.length());
		
		
		
		
		
		
	}
}